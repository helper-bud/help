var vs dynamic 
1. var is once assigned and the data type cant be changed. means if initially we set int value, it will be int the whole time
2. Dynamic variable type can be changed over time . 

  void main(){
  var a = 10; // cant c
  a = 20;
  a = "erro"; // shows error.

  dynamic b = 20;
  b = 'bob'; // no error. 
}
